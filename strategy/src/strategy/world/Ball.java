package strategy.world;

public class Ball
{
	
	private double x;
	private double y;
	private int visible;

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public int getVisible() {
		return visible;
	}

	public void setX(double new_x) {
		x = new_x;
	}

	public void setY(double new_y) {
		y = new_y;
	}

	public void setVisible(int vis) {
		visible = vis;
	}
	
}
